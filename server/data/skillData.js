export const SKILL_BY_ID = Object.fromEntries(SKILL_DEFINITIONS.map((skill) => [skill.id, skill]));

export function getSkillDefinition(skillId) {
  return SKILL_BY_ID[skillId] ?? null;
}

export function getSkillDefinitions() {
  return SKILL_DEFINITIONS;
}

export function getSkillFamilies() {
  return Array.from(new Set(SKILL_DEFINITIONS.map((skill) => skill.family)));
}
