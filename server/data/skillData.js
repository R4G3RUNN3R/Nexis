function activeSkill({
  id,
  name,
  family,
  tier,
  summary,
  starter = false,
  requiredCourses = [],
  requiredFlags = [],
  requiredSkills = [],
  requiredAcademies = [],
  xpToEvolve = null,
  evolvesTo = null,
  combat = {},
  useXp = 10,
}) {
  return {
    id,
    name,
    family,
    slotType: "active",
    kind: "active",
    tier,
    summary,
    starter,
    requiredCourses,
    requiredFlags,
    requiredSkills,
    requiredAcademies,
    xpToEvolve,
    evolvesTo,
    combat: {
      damageMultiplier: 1,
      accuracyBonus: 0,
      critBonus: 0,
      evadeBonus: 0,
      mitigationBonus: 0,
      heal: 0,
      ...combat,
    },
    useXp,
  };
}

