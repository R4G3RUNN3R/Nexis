const FIVE_MINUTES = 5 * 60 * 1000;
const CONTRACT_REFRESH_MS = 20 * 60 * 1000;

function item(itemId, label, quantity = 1) {
  return { itemId, label, quantity };
}

function contract({
  id,
  cityId,
  title,
  type,
  summary,
  risk = "low",
  requirementLabel,
  staminaCost = 1,
  visitCityId = null,
  visitLabel = null,
  reward,
  standingReward = 1,
  minimumStanding = 0,
  combat = null,
  chronicle,
}) {
  return {
    id,
    cityId,
    title,
    type,
    summary,
    risk,
    requirementLabel,
    minimumStanding,
    standingReward,
    refreshMs: CONTRACT_REFRESH_MS,
    completion: {
      staminaCost,
      visitCityId,
      visitLabel,
      note: visitCityId
        ? `Visit ${visitLabel}, then return to the issuing city to close the contract.`
        : `Complete the work while present in the issuing city.`,
    },
    reward,
    combat,
    chronicle,
  };
}

