function stage({ id, title, summary, requiredCourses = [], requiredStanding = 0, reward, standingReward = 2, chronicle }) {
  return {
    id,
    title,
    summary,
    durationMs: FIVE_MINUTES,
    requiredCourses,
    requiredStanding,
    entryRequirements: [
      requiredStanding ? `${requiredStanding} local standing` : "Local presence",
      ...requiredCourses.map((courseId) => `Complete ${courseId}`),
    ],
    reward,
    standingReward,
    chronicle,
  };
}

function academy({ id, cityId, name, theme, progressionSupports, lockReason, stages }) {
  return { id, cityId, name, theme, progressionSupports, lockReason, stages };
}

